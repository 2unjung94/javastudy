package pkg01_wrapper_class;

public class MainClass {

  public static void main(String[] args) {

    // Auto Boxing (기본 타입 값 -> Wrapper 타입 값)
    Integer num = 10;   // 타입은 Integer. 메모리 상에서는 저장값은 10이 저장된 참조값. 
    
    // Auto Unboxing ( Wrapper 타입 값 -> 기본 타입 값)
    int iNum = num;
    
    
    
  }

}
